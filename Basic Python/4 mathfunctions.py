import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) #pi variable rounded to the nearest integer.
print(math.ceil(pi)) #rounds variable up.
print(math.floor(pi)) #rounds variable down.
print(abs(pi)) #tells you how far a number is away from 0.
print(pow(pi,2)) #will raise a base number to a power, enter in base (variable), then power.
print(math.sqrt(81)) #finds the square root of a number or variable.
print(max(x,y,z)) #finds the largest value in a set of variables.
print(min(x,y,z)) #finds the lowest value in a set of variables.